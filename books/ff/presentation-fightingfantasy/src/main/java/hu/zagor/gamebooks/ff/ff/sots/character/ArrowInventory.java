package hu.zagor.gamebooks.ff.ff.sots.character;

/**
 * Inventory of the arrows we have with us.
 * @author Tamas_Szekeres
 */
public class ArrowInventory {
    private int willowLeaf;
    private int bowelRaker;
    private int armourPiercer;
    private int hummingBulb;
    private int tsunevara;

    public int getWillowLeaf() {
        return willowLeaf;
    }

    public void setWillowLeaf(final int willowLeaf) {
        this.willowLeaf = willowLeaf;
    }

    public int getBowelRaker() {
        return bowelRaker;
    }

    public void setBowelRaker(final int bowelRaker) {
        this.bowelRaker = bowelRaker;
    }

    public int getArmourPiercer() {
        return armourPiercer;
    }

    public void setArmourPiercer(final int armourPiercer) {
        this.armourPiercer = armourPiercer;
    }

    public int getHummingBulb() {
        return hummingBulb;
    }

    public void setHummingBulb(final int hummingBulb) {
        this.hummingBulb = hummingBulb;
    }

    public int getTsunevara() {
        return tsunevara;
    }

    public void setTsunevara(final int tsunevara) {
        this.tsunevara = tsunevara;
    }
}
